#include <unistd.h>
#include "../checker.h"
#include "../libft/libft.h"

int	ps_free(t_list_num **list)
{
    t_list_num *tmp;
	while (*list)
    {
        tmp = *list;
        free(*list);
        *list = tmp->next;
    }
    return (1);
}
